public class Processa1{
	public static void main(String[] args){
		//Tipos primitivos
		//ASSCCI 
		char c1 = 'x';
		//inteiros
		byte b1 = 25;
		short s1 = 31548;
		int il = 2456877;
		long lol = 879879879;
		//lógico
		boolean bo1 = false;
		//ponto flutuante
		float f1 = 25.45f;
		double d1 = 45687987.456d;
		System.out.print(c1 +"\n"+ b1 +"\n"+ s1 +"\n"+ il +"\n"+ lol +"\n"+ bo1 +"\n"+ f1 +"\n"+ d1 +"\n");
		System.out.printf("A variável cl tipo char é: %c \n", c1);
		System.out.printf("A variável b1 tipo byte é: %d \n", b1);
		System.out.printf("A variável s1 tipo short é: %d \n", s1);
		System.out.printf("A variável il tipo int é: %d \n", il);
		System.out.printf("A variável lol tipo long é: %d \n", lol);
		System.out.printf("A variável bo1 tipo boolean é: %s \n", bo1);
		System.out.printf("A variável f1 tipo float é: %.2f \n", f1);
		System.out.printf("A variável d1 tipo double é: %.3f \n", d1);
		
		
	}
}