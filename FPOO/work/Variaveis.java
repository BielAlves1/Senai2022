public class Variaveis{
	public static void main(String[] args){
		//Exemplo do tipo byte
		byte byte1 = 65;
		byte byte2 = 66;
		int soma = byte1 + byte2;
		System.out.println(soma);
		
		//Exemplo do tipo char
		char char1 = 65;
		char char2 = 66;
		String soma2 = char1 + " e " + char2;
		System.out.println(soma2);
		
		System.out.println("A soma de dois bytes é: "+ soma);
		System.out.println("A soma de dois caracteres é: "+ soma2);
	}
}